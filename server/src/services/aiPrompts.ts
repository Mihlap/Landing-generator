type SiteLocale = "ru" | "en";

export type MapEmbedProvider = "yandex" | "google";

export function inferMapEmbedProviderFromPrompt(prompt: string, locale: SiteLocale): MapEmbedProvider {
  let cyrillic = 0;
  let latin = 0;
  for (const ch of prompt) {
    const cp = ch.codePointAt(0)!;
    if (cp >= 0x0400 && cp <= 0x04ff) cyrillic++;
    else if ((cp >= 0x0041 && cp <= 0x005a) || (cp >= 0x0061 && cp <= 0x007a)) latin++;
  }
  if (cyrillic > latin) return "yandex";
  if (latin > cyrillic) return "google";
  return locale === "ru" ? "yandex" : "google";
}

